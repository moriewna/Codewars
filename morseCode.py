#MORSE CODE
MORSE_CODE = {'.-...': '&', '--..--': ',', '....-': '4', '.....': '5', \
'...---...': 'SOS', '-...': 'B', '-..-': 'X', '.-.': 'R', '.--': 'W', \
'..---': '2', '.-': 'A', '..': 'I', '..-.': 'F', '.': 'E', '.-..': 'L', \
'...': 'S', '..-': 'U', '..--..': '?', '.----': '1', '-.-': 'K', '-..': 'D', \
'-....': '6', '-...-': '=', '---': 'O', '.--.': 'P', '.-.-.-': '.', '--': 'M', \
'-.': 'N', '....': 'H', '.----.': "'", '...-': 'V', '--...': '7', '-.-.-.': ';',\
 '-....-': '-', '..--.-': '_', '-.--.-': ')', '-.-.--': '!', '--.': 'G', \
 '--.-': 'Q', '--..': 'Z', '-..-.': '/', '.-.-.': '+', '-.-.': 'C', \
 '---...': ':', '-.--': 'Y', '-': 'T', '.--.-.': '@', '...-..-': '$', \
 '.---': 'J', '-----': '0', '----.': '9', '.-..-.': '"', '-.--.': '(', \
 '---..': '8', '...--': '3'}

def decodeMorse(morseCode):
    return "".join([" " if word == "!" else MORSE_CODE[word] for word in \
    morseCode.replace('  ',' ! ').split()]).strip()

